class Solution {
  public:
    int pairWithMaxSum(vector<int>& arr) {
        if(arr.size()==1) return -1;
        int ans= arr[0]+ arr[1];
        int tmp;
        for(int i=2;i<arr.size();i++){
            tmp=arr[i]+arr[i-1];
            ans=max(ans,tmp);
        }
        return ans;
    }
};

